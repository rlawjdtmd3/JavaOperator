public class ForLoopTest {
	public static void main(String[] args) {//argument 메인메소드 디폴트값 ,, 아래 args에 그대로 끌고옴
		for (int i = 0; i < 10; i++) {
			System.out.print((i+1)+"\t");
			//이런식으로 초기값은 안거드리고 for문 안에값을 건드리는게 좋음
		
		}
	}
}
